// Заговори що підходять під поточну фазу місяця
function getSpellsByMoonPhase(phaseEnergy, limit = 6) {
  const matching = SPELLS.filter(s =>
    s.moon.includes(phaseEnergy) || s.moon.includes('any')
  );
  // Змішуємо і повертаємо
  return matching.sort(() => Math.random() - 0.5).slice(0, limit);
}

function getSpellsByCategory(categoryId, isPremium = false) {
  return SPELLS.filter(s =>
    s.category === categoryId && (isPremium || !s.premium)
  );
}

function getSpellById(id) {
  return SPELLS.find(s => s.id === id) || null;
}

